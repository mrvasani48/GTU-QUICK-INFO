                    <p style="text-align:justify">Gujarat Technological University is a premier academic and research institution which has driven new ways of thinking since its 2007 founding, established by the Government of Gujarat vide Gujarat Act No. 20 of 2007. Today, GTU is an intellectual destination that draws inspired scholars to its campus, keeping GTU at the nexus of ideas that challenge and change the world.   GTU is a State University with 486 affiliated colleges in its fold operating across the state of Gujarat through its FIVE zones at Ahmedabad, Gandhinagar, Vallabh Vidyanagar, Rajkot and Surat. The University caters to the fields of Engineering, Architecture, Management, Pharmacy and Computer Science. The University has about 4,00,000 students enrolled in a large number of Diploma, Under Graduate, Post Graduate programs along with the robust Doctoral program.
                       </p>
                       <p style="text-align:justify">Our education empowers individuals to challenge conventional thinking in pursuit of original ideas. With a commitment to free and open inquiry, our scholars work transform the way we understand the world, advancing – and creating – fields of study.    
                       </p>
                       <h3>The Vision, Mission statements and the Objectives it stands to fulfil are:</h3>
                        <h5>  VISION</h5>
                        <p style="text-align:justify"> &nbsp;&nbsp;  To make Gujarat Technological University a World Class University.</p>
                        <h5> MISSION</h5>
                        <p style="text-align:justify"> &nbsp;&nbsp;Every single stakeholder of the University should find pleasure in working with GTU.
                         </p>
                         <h5> OBJECTIVES</h5>
                         <p style="text-align:justify"> Make our operations transparent and acceptable to all stakeholders.<br>
                         &nbsp;&nbsp; To provide quality education, training, vocation and research facilities to our students.<br>
                         &nbsp; To continuously organize and manage Faculty Development Programs (FDPs),Seminars and Conferences.<br>
                         &nbsp;&nbsp; Affiliate and Coordinate with Colleges for an effective education delivery mechanism.<br>
                         &nbsp;&nbsp; Timely and efficient conduct of the Examination process.<br>
                         &nbsp;&nbsp; To facilitate student’s placements into suitable and meaningful careers and future of their choice.<br>
                         </p>
                         <p style="text-align:justify">GTU has emerged as an International Innovative University in its pursuit of bringing innovation and internationalization in professional education. Within a really short span it has achieved several national accolades for its endeavour in bringing excellence in professional education. GTU is a pioneer in introducing some innovative learning methodology like “Active Learning”, a classroom created online. The GTU Innovation Council is the most active and applauded for its work in the country engaged in training, developing and nurturing the young minds towards an ideology to innovate. GTU has the largest International Experience Program in collaboration with the universities of US, Canada, China and Germany, which offer a unique opportunity to the students to enhance their capabilities and capacities in a global perspective. GTU’s Research Week, a unique concept, is an evaluation process of dissertations of Master’s and Doctoral Program students involving Experts from the Universities across the Globe. GTU’s Contributor Personality Development Program and the Bridge Course nurture the students with the essential life skills along with the technical knowledge to enable them develop as an individual and a successful professional in the competitive world. The Industrial Training and Placement Cell transforms a knowledge seeker into a sought after professional.</p>
                         <p style="text-align:justify">The Post <b>Graduate Research Centres</b> at GTU are actively involved in several national and international research projects. Each year, GTU hosts several national and international conferences to bring together the academicians and industry experts on a single platform for the exchange of knowledge, experiences and best practices in their area of working. GTU has partnered with leading industries and associations like Bosch Rexroth, Oracle, IEEE, NSE, BSE, C-DAC and many more to encourage industry-academia knowledge exchange.</p>
