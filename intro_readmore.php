                 <h2 align="center" style="margin-bottom: 0px;">MESSAGE FROM THE VICE-CHANCELLOR</h2><br>
                 <p class="pe" >I hope you are safe and following norms of social distancing as a precautionary measure of covid-19. We have seen many drastic changes in the duration of January to May 2020. For GTU, the year 2020 started on a very positive note by organizing 9th annual convocation at Mahatma Mandir in the presence of Shri Amit Shah, Hon’ble Home Minister of India. His gracious presence motivated thousands of students. Ironically March end saw the lockdown period due to Covid -19 pandemic and all affiliated institutes were immediately closed. To ensure the safety of all students, an online teaching was started by all the institutes using digital platforms to minimize the academic loss.</p>
                 <p class="pe">GTU organized more than 100 webinars / expert talks, Mental Health Counselling Helpline, online lecture series, online fitness training programmes and classical music programmes during lockdown. University ensured online teaching learning for the students by posting more than 3000 recorded video lectures/ presentations by respective subject experts of affiliated colleges on GTU website (http://lectures.gtu.ac.in/) and it’s YouTube Channel.</p>
                 <p class="pe">Young students of GTU Innovation Council have developed innovative products like 3D printed face shield; drones to disinfect common areas, social monitoring and delivery of critical supplies; wireless robot to help nurses and medical staff; social distancing belt; smart multi-layer gloves to stop spreading corona virus; small fully automatic machine to burn used face mask, hand gloves, etc. and sanitizing chamber to keep us free from virus. GTU has developed a first-time novel method with the Fourier Transform – Infrared Spectrophotometer (FT-IR) for the quality testing of alcohol-based hand sanitizer. Hand sanitizer has also been developed by professors of GTU as per the specification of World Health Organization (WHO).</p>
                 <p class="pe">On 16th May 2020, GTU celebrated online 13th Foundation Day in the august virtual presence of Shri Vijaybhai Rupani, Hon’ble Chief Minister, Gujarat; Shri Bhupendrasinh Chudasama, Education minister, Govt. of Gujarat and Smt. Vibhavariben Dave, Minister of State for Education, Govt. of Gujarat. I am delighted to share that recently GTU received ‘Most Promising University of India 2020 Award’ by Integrated Chambers of Commerce and Industry (ICCI) which indeed motivated me to continue the best practices of the university. GTU has got the 1st rank in Gujarat and 28th rank in the list of top 200 universities of the country. The credit of success goes to all stakeholders of the GTU and I hope that GTU will set new records in coming years.</p>
                 <p class="pe">GTU is going to conduct ‘Summer Examination 2020’ for final semester of UG programmes and all semesters of PG programmes with all safety measures at various centres across the state and I extend my best wishes to all students for examination. I urge all of you to remain safe and wish you healthy life. Thank you</p>
